 <h4>Elements of Book Publishing</h4>
 <hr>
<h3>Abstract:</h3>
<p align="justify">

The importance of research publishing can be defined by a simple quote of Gerard Piel, which says “Without publication, science is dead.” The story of book publishing started more than 1100 years ago and it is further revolutionized by the invention of first printing press in 1454 by Johannes Gutenberg. In the last 20 years, science and reporting of science have undergone revolutionary changes. Computerization and Internet have changed the traditional ways of reading and writing. Hence, it is very important for scientists and students of the sciences in all disciplines to understand the complete process of writing books and their types. A famous American author, Stephen Edwin King, has correctly expressed the beauty of writing with his quote “To write is human, to edit is divine.” The talk is designed to provide information on different elements of book publishing.
Aninda Bose is presently working as a Senior Publishing Editor with Springer Nature. Mr. Bose is part of the Global Acquisition Team at Springer Nature and responsible for acquisition of scientific content across the globe. He is responsible for acquisition of content in Interdisciplinary Applied Sciences. He has more than 26 years of industrial experience in marketing and different fields of publishing. Mr. Bose has completed Masters in Organic Chemistry from Delhi University and Masters in Marketing Research from Symbiosis Institute of Management Studies, Pune. Mr. Bose has delivered more than 140 invited talks on Scientific Writing and Publishing Ethics in reputed Universities, International Conferences and Author Workshops. He has published books for secondary level in Chemistry and is a member of American Chemical Society, USA.

