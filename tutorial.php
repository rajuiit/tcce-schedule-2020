 <h4>Apache Hadoop and MapReduce: A Doorway to Big Data Analytics using Distributed Computing
</h4>
 <hr>
<h3>Abstract:</h3>
<p align="justify">

Over the last few years, technological evolution has introduced on the market unprecedented number of devices capable of not only generating but also requesting analysis of large quantities of data. Traditionally, the difficulty in managing these data increased with the need of mining value from them; for this reason, the Big Data world has taken a dominant role within modern information technology. Big Data technology research and development projects have become increasingly common, and consequently, also their application fields have always increased more.
The Apache Hadoop and MapReduce are two main technologies that has paved the pathway of modern-day Big Data analytics tools and platforms. This tutorial will introduce these two technologies along with a step-by-step illustration of: Installing a single node Hadoop system in a local computer; and An example of distributed computing using MapReduce.
