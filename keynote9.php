 <h4>Bioimage informatics plays pivotal role to answer fundamental biological questions 

</h4>
 <hr>
<h3>Abstract:</h3>
<p align="justify">

Our research efforts focus on continuous advancement of our understanding of the fundamental processes of life. We perform state-of-the-art microscopy across scales to capture various events inside living organisms and cells. Automated analysis of this multi-dimensional data is key to quantify the structures and functions associated with the target study in order to understand how cells and their molecular machines are constructed and dynamically work together to carry out the essential processes of life. Automated analysis involves substantial development of sophisticated computational frameworks to extract important information while dealing with complexity and heterogeneity of the data from various imaging modalities. In this talk, I will make use of few important biological studies to demonstrate how bioimage informatics approaches, a sub-field in computational biology focusing on bioimage analysis, played key role to answer fundamental research questions. I will also discuss why bioimage informatics is so important now and how this sub-field is attracting increasing focus in multi-disciplinary research.
Dr. M. Julius Hossain is a Research Scientist in the Cell Biology and Biophysics Unit at the European Molecular Biology Laboratory (EMBL), Germany. Dr. Hossain was trained as an Engineer and obtained his PhD degree in Computer Engineering with a major in computer vision and image processing from Kyung Hee University, South Korea. He did his postdoctoral research in biomedical image analysis in Dublin City University, Ireland and then moved to EMBL where he has been very active in interdisciplinary research for many years. Dr. Hossain actively collaborates with scientists in several other disciplines including biology, physics, chemistry, and bioinformatics to develop computational methods for quantifying and modeling biological systems specially the dividing human cell and mouse embryo. Dr. Hossain had spent more than a year in the industry as a Software Engineer before he started his academic career at the University of Dhaka where he served as a full time Lecturer and then Assistant Professor in the Department of Computer Science and Engineering. His current research focus includes bioimage informatics, modeling cell and embryo shape, parameterization of dynamic protein distributions in the dividing human cell.





