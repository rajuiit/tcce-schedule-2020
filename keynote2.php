 <h4>Deep Learning Methodologies and Their Applications in Computational and Cognitive Engineering</h4>
 <hr>
<h3>Abstract:</h3>
<p align="justify">
In the era of artificial intelligence (AI), traditional computational and cognitive systems are being transformed into intelligent AI systems. Among various machine learning methodologies, deep learning is leading this advancement of various AI systems. In this presentation, the basic principles of major deep learning methodologies such as convolutional neural networks, recurrent neural networks, auto-encoders, and reinforcement learning will be briefly introduced. Then their applications in computational and cognitive engineering will be presented including biomedical computer-aided diagnostic systems (CAD), human activity recognition in life care systems, and humanoid robotics for natural object manipulation. 
About: Tae-Seong Kim received the B.S. degree in Biomedical Engineering from the University of Southern California (USC) in 1991, M.S. degrees in Biomedical and Electrical Engineering from USC in 1993 and 1998 respectively, and Ph.D. in Biomedical Engineering from USC in 1999. After his postdoctoral work in Cognitive Sciences at the University of California at Irvine in 2000, he joined the Alfred E. Mann Institute for Biomedical Engineering and Dept. of Biomedical Engineering at USC as Research Scientist and Research Assistant Professor. In 2004, he moved to Kyung Hee University in Republic of Korea where he is currently Professor in the Department of Biomedical Engineering. His research interests have spanned various areas of biomedical imaging, bioelectromagnetism, neural engineering, and assistive lifecare technologies. Dr. Kim has been developing novel methodologies in the fields of signal and image processing, machine learning, pattern classification, and artificial intelligence. Lately Dr. Kim has started novel projects in the developments of smart robotics and machine vision with deep learning methodologies. Dr. Kim has published more than 350 papers and twelve international book chapters. He holds ten international and domestic patents and has received numerous best paper awards.

