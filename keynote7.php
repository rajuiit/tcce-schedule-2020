 <h4>Web Intelligence meets Brain Informatics
</h4>
 <hr>
<h3>Abstract:</h3>
<p align="justify">

Network, in particular W2T (Wisdom Web of Things, i.e. Web Intelligence = AI in the connected world) developed recently, provides a cyber-physical-social space for all human communications and activities, in which big data are used as a bridge to connect relevant aspects of humans, computers and things. It is a trend to integrate brain big data and human behavior big data with knowledge graph in the cyber-physical-social space for realizing the harmonious symbiosis of humans, computers, and things. In this talk, I demonstrate a Brain Informatics based systematic approach to an integrated understanding from brain and mental health to brain-machine intelligence in the connected world. I discuss research issues and challenges with respect to brain intelligence, brain health and brain internet, including how to understand brain from neural microcircuits to macroscale intelligence systems, supported by connecting network and brain with big data; how to realize human-level collective intelligence as a big data sharing mind on the W2T by brain big data computing for developing brain inspired technologies to provide wisdom services.
Ning Zhong received the Ph.D. degree from the University of Tokyo. He is currently head of Knowledge Information Systems Laboratory, and a professor in Department of Life Science and Informatics at Maebashi Institute of Technology, Japan. He is also a director and an adjunct professor in the International WIC Institute (WICI), Beijing University of Technology. Currently, Dr. Zhong is focusing on “Web Intelligence (WI) meets Brain Informatics (BI)” research and brain big data computing. The synergy between WI and BI advances our ways of analyzing and understanding of data, information, knowledge, wisdom, as well as their interrelationships, organizations, and creation processes, to achieve human-level Web intelligence (AI in the connected world) reality. Such interdisciplinary studies make up the field of brain informatics and its applications in brain intelligence, brain health, brain internet and brain-inspired technologies among others. Dr. Zhong is the founding editor-in-chief of Web Intelligence journal (IOS Press), the editor-in-chief of Brain Informatics journal (Springer Nature), the editor-in-chief of Brain Informatics & Health (BIH) book series (Springer Nature) and serves as associate editor/editorial board for several international journals and book series. Dr. Zhong is the co-founder and co-chair of Web Intelligence Consortium (WIC), chair of the IEEE Computational Intelligence Society Task Force on Brain Informatics, co-founder and steering committee co-chair of IEEE/WIC/ACM International Conference on Web Intelligence (WI), and co-founder and steering committee co-chair of International Conference on Brain Informatics (BI).


 


