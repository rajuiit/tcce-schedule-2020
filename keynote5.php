 <h4>Failures of e-Government projects in low-income countries
</h4>
 <hr>
<h3>Abstract:</h3>
<p align="justify">

Failures of e-Government projects in low-income countries are well-documented, though for many countries the adoption of information and communication technologies present the most effective approach to overcoming some of the challenges faced in service provision in critical sectors such as agriculture and healthcare. However, the long-term sustainability of e-Government projects may be hampered by an inadequate evaluation of the social and technical environment in which these solutions are placed, particularly with respect to the role and engagement of stakeholders during project design and development. This presentation discussion the application of a stakeholder engagement framework in the implementation processes of e-Government projects in low-income countries.   The framework highlights a multi-dimensional approach to e-Government design with a focus on the engagement of stakeholders for successful implementation and the capitalisation of available resources.
 
Dr Frost is a senior lecturer in the Department of Management, Nottingham Trent University.  Her teaching interests cuts across Business Analytics and Information Systems, with responsibility for module leadership on a number of undergraduate and post-graduate modules.  Diana also supervises undergraduate and postgraduate (MSc/PhD/DBA) research.   
Prior to her present employment at Nottingham Trent University, and on completion of her PhD studies, Dr Frost owned and managed a business consulting company.  The company provided consulting services (primarily in the information and communication technologies sector), for a number of governments, small businesses and international agencies.  Her current research focuses on the use of technologies for socio-economic development in low-income countries particularly their application within the healthcare sector. Diana has a PhD (interdisciplinary) in Computer Science, Politics and Policy (Tufts University), MSc in Computer Science (University of Oxford), and BSc (first class honors) in Computer Science and Mathematics (University of the West Indies).
 


