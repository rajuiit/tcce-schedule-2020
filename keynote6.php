<h3>Abstract:</h3>
<p align="justify">

A human-like stereo sensor is designed using plasmonic integrated circuits, which is formed by 6 serial antenna structure embedded in a Mach Zehnder Interferometer. It can be applied for brain-device interfacing, from which the deep brain signals can be retrieved from the sensor outputs. Both electrical and quantum signals can be obtained by using this technique. The preliminary results are presented and discussed, especially for quantum sensing, from which the stereo sensors will be applied for a two-level system, where the Rabi oscillation has occurred, which is claimed as a quantum signal that can be used for quantum consciousness applications. In this work, the plasmonic stereo sensor circuit is formed by a silicon microring resonator embedded with silver grating. The electric field is input via an input port, while the add port is input by the modulated energy time function. The circuit operates under successive filtering and pumping mechanism. When it reaches the steady state, the resonant output signals can be obtained at the through and drop ports. The required output is in the form of the whispering gallery mode (WGM), which can be obtained by adjusting the optical path difference of the two side phase modulators. Light propagates into the different device lengths. The optical path difference is formed, where the material refractive index is the key parameter. The WGM is formed by the plasma oscillation of the trapped electrons (polaritons), where light behavior becomes a quantum phenomenon. The system is configured by the two-level system (n=2), which is known as the harmonic oscillator. The required oscillation frequency called Rabi oscillation can occur, where there are two different frequencies (energy states). Space and time distortions are compensated and matched by the resonant optical path differences within the circuit, which both have vanished. From a waveparticle equation, the stereo sensor can be formed when the space-time modulation control. The required output is obtained when space-time distortion is approached zero, the WGM output with n=2 is the two-level stereo system. In manipulation, the change in the input power causes the change in the electron density, which can be received at the output ports. The quantum signals(codes) can be obtained by the polarized electron density projections, which can be used for quantum sensor and deep learning code/decode applications.
Preecha Yupapin received the Ph.D. degree in electrical engineering from the City, University of London, UK in 1993. He is currently the full Professor in the Computational Optics Research Group, Advanced Institute of Materials Science and member in the Faculty of Applied Sciences, Ton Duc Thang University, Ho Chi Minh City, Vietnam. He has published 650 Scopus indexed papers, 15 international book editions. His current research interests are nano-devices and circuits, microring resonator, soliton communication, optical motor, quantum technologies, and quantum meditation.

 


