 <h4>How AI-ML is transforming the Telecom Industry</h4>
 <hr>
<h3>Abstract:</h3>
<p align="justify">

The telecom industry is going through a transformation. It is not the first time this industry is experiencing changes, but it has never been so rapid and disruptive as now. The rise and fall of a telecom operator is being determined by its ability of built-to-adapt, not built-to-last. While many telecom players are struggling to cope up with the tsunami of changes bottom-up, there are quite a few who have been able to adapt to the changing business needs, cost structures, and skillsets to see growth. If there is one common element of success among these type two players, it is the accelerated use of AI-ML in both decision making and decision fulfillment, what we call going full-circle.
The adaption of AI-ML in the Telecom Industry has cut across business and technology layers alike, cultural and operational areas alike, and trial and production cycles alike. It has proved important for the industry to fail fastest, learn faster, get result fast when trying out AI-based use-cases. Cloud became Cloud-ML, Statistical models gave way to ML models, Manual work got replaced by RPA and ML-Workflows. The results were increased efficiency, reduced margin of errors, improved customer experience, lowered cost of operation, diversified revenue portfolio, and a future-ready workforce. The talk includes some real-life use-cases from the Telecom Industry as experienced first-hand. 
Dr. Asif Naimur Rashid is the CIO of Robi Axiata, the second largest Mobile Operator in Bangladesh and a subsidiary of Axiata Group, Malaysia. He also has an additional role being the Managing Director of the Startup RedDot Digital Limited, a 100% owned subsidiary of Robi. Dr. Asif has been working in the industry for 20 years now. He is successfully leading the key digital transformation initiatives of Robi and Axiata group. Prior to joining Robi Axiata, he has worked in Telenor Myanmar, Telenor HQ, Grameenphone, Siemens AG and Mentors’ Technologies. Dr. Asif has a bachelor’s degree with hon’s in Applied Physics & Electronics from the University of Dhaka, a master’s degree in Telecommunication Engineering from University of Technology, Sydney, an MBA in Executive Management from Royal Roads University, Vancouver, British Columbia, and a Doctoral degree on Artificial Intelligence from California Southern University, California. Aside, he has attended NUS, INSEAD Business School & MIT Sloan School of Management for advanced technology, digital strategy, and leadership programs and regularly speaks at national and international technology events. In his personal life, Dr. Asif is married and blessed with three children. 


